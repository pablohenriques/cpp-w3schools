#include <iostream>
#include <cmath>

using namespace std;


int main() {
    /*Funções Matemáticas*/

    /*Maior valor entre dois números*/
    cout << max(1, 10) << endl;

    /*Menor valor entre dois números*/
    cout << min(-1, 10) << endl;

    /*De 'cmath': raiz quadrada, arredondamento, logaritmo*/
    cout << "Raiz quadrada de 64: " << sqrt(64) << endl;
    cout << "Arredondamento de 2.6: " << round(2.6) << endl;
    cout << "Logaritmo de 2: " << log(2) << endl;

    return 0;
}